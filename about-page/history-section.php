  <h2 class="text-3xl font-bold text-gray-800 mb-8 text-center">Our History</h2>

  <div class="space-y-12">

    <!-- 1988 -->
    <div>
      <h3 class="text-2xl font-semibold text-green-700 mb-4">1988</h3>
      <ul class="space-y-3 border-l-2 border-green-500 pl-6">
        <li>
          <span class="font-bold">January 1988 –</span> Founding group met at Mandarin Oriental Hotel, Makati, to form an informal “breakfast club” of animal nutritionists.
        </li>
        <li>
          <span class="font-bold">June 28, 1988 –</span> Constitution & by-laws approved (32 charter members, 2 associate members). First officers elected.
        </li>
        <li>
          <span class="font-bold">Mid-1988 –</span> PHILSAN logo adopted and the Society formally launched at Hotel Nikko Manila Garden.
        </li>
        <li>
          <span class="font-bold">September 19, 1988 –</span> First seminar: “An Audience with the Maestro” by Dr. Milton L. Scott, held at Hotel Intercontinental Manila.
        </li>
        <li>
          <span class="font-bold">October 26, 1988 –</span> First international seminar-convention at Manila Polo Club with 5 foreign speakers and 150+ participants.
        </li>
      </ul>
    </div>

    <!-- 1989 -->
    <div>
      <h3 class="text-2xl font-semibold text-green-700 mb-4">1989</h3>
      <ul class="space-y-3 border-l-2 border-green-500 pl-6">
        <li>
          <span class="font-bold">April 21–22, 1989 –</span> Nutrient Standardization Workshop held at Puerto Azul, Cavite. Ad hoc committees formed.
        </li>
        <li>
          <span class="font-bold">September 29, 1989 –</span> Five new regular members admitted.
        </li>
        <li>
          <span class="font-bold">October 18, 1989 –</span> Second PHILSAN seminar-convention at Manila Polo Club: “Animal Nutrition in the 90’s.” New officers inducted.
        </li>
      </ul>
    </div>

    <!-- 1990 -->
    <div>
      <h3 class="text-2xl font-semibold text-green-700 mb-4">1990</h3>
      <ul class="space-y-3 border-l-2 border-green-500 pl-6">
        <li>
          <span class="font-bold">April 26–27, 1990 –</span> Follow-up Nutrient Standardization Workshop at Puerto Azul, Cavite. Progress reports presented.
        </li>
        <li>
          <span class="font-bold">December 7, 1990 –</span> Launch of <em>PHILSAN Recommends</em> handbook.
        </li>
        <li>
          <span class="font-bold">1990 –</span> First Edition of the PHILSAN Feed Reference Standards published (focused on poultry & swine).
        </li>
      </ul>
    </div>

    <!-- 1996 -->
    <div>
      <h3 class="text-2xl font-semibold text-green-700 mb-4">1996</h3>
      <ul class="space-y-3 border-l-2 border-green-500 pl-6">
        <li>
          <span class="font-bold">1996 –</span> Second Edition of the PHILSAN Feed Reference Standards published, expanding to poultry, swine, fish, ruminants, and new ingredients.
        </li>
      </ul>
    </div>

    <!-- 2003 -->
    <div>
      <h3 class="text-2xl font-semibold text-green-700 mb-4">2003</h3>
      <ul class="space-y-3 border-l-2 border-green-500 pl-6">
        <li>
          <span class="font-bold">2003 –</span> Third Edition of the PHILSAN Reference Standards launched (10 chapters, including laws regulating the feed industry).
        </li>
      </ul>
    </div>

    <!-- 2009 -->
    <div>
      <h3 class="text-2xl font-semibold text-green-700 mb-4">2009</h3>
      <ul class="space-y-3 border-l-2 border-green-500 pl-6">
        <li>
          <span class="font-bold">2009 –</span> Launch of <em>PHILSAN Feed & Nutrition Digest</em>, the official newsletter.
        </li>
      </ul>
    </div>

    <!-- 2010 -->
    <div>
      <h3 class="text-2xl font-semibold text-green-700 mb-4">2010</h3>
      <ul class="space-y-3 border-l-2 border-green-500 pl-6">
        <li>
          <span class="font-bold">August 2010 –</span> Launch of the official PHILSAN website.
        </li>
      </ul>
    </div>

  </div>
